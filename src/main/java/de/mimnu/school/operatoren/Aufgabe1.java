package de.mimnu.school.operatoren;

public class Aufgabe1 {
    
    public static void main(String args[]) {
		int a = 299, b = 48;
		
		System.out.println(a + " + " + b + " = " + (a+b));
		
		System.out.println(a + " - " + b + " = " + (a-b));
		
		System.out.println(a + " * " + b + " = " + (a*b));
		
		System.out.println(a + " / " + b + " = " + (a/b));
	}
}
